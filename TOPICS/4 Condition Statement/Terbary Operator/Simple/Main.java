public class Main{

     // ternary operator ? = return 1 of 2 value if a condition is true

        // variable  =  (condition) ? iftrue : ifFalse
    public static void main(String[] args) {
        int age = 20;

        String result = (age >= 18) ? "Adult" : "Minor";

        System.out.println("You are: " + result);
    }
}
